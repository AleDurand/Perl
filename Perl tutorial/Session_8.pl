# Operador de seleccion

# Operador de seleccion es un operador triario que permite hacer una cosa u otra
# dependiendo de la condicion dada, se puede describir como el equivalente
# a una condicional simple con las sentencias if else

# Estructura: condicion ? accion1: accion2
use strict;

my $variable1 = 2;
my $variable2 = 4;

$variable1 < $variable2 ? print "OK" : print "ERROR";
