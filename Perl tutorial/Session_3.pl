# Tipos de datos

use strict;

# string

my $string = "Hello world" . "\n";
print $string;

$string = 'Hello world' . '\n';
print $string;

print "\n";

my $name = "Alejandro";
my $string2 = "Hello $name \n";
print $string2;

# integer

my $entero = 230;
print $entero . "\n";

# float

my $decimal = 2.34;
print $decimal . "\n";

# octal
my $octal = 033445566;
print $octal . "\n";

# hexadecimal
my $hexadecimal = 0xFF00FF11;
print $hexadecimal . "\n";
